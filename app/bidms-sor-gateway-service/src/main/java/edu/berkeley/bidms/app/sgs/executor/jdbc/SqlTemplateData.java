package edu.berkeley.bidms.app.sgs.executor.jdbc;

import java.util.Map;

/**
 * This is injected as the data model while parsing a SQL template.
 */
public class SqlTemplateData {
    private String whereClause = "";
    private Map<String, String> ext;

    public SqlTemplateData(Map<String, String> ext) {
        this.ext = ext;
    }

    public SqlTemplateData(String whereClause, Map<String, String> ext) {
        this.whereClause = whereClause;
        this.ext = ext;
    }

    public String getWhereClause() {
        return whereClause;
    }

    public void setWhereClause(String whereClause) {
        this.whereClause = whereClause;
    }

    public Map<String, String> getExt() {
        return ext;
    }

    public void setExt(Map<String, String> ext) {
        this.ext = ext;
    }
}
